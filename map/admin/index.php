// ── Load live config.yaml from server ────────────────────────────────────────

async function doLoad() {
    try {
        // First, check for current event in localStorage (from map page)
        let cfg = null;
        let filename = '';
        try {
            const stored = localStorage.getItem('aprs_current_event');
            if (stored) {
                const parsed = JSON.parse(stored);
                cfg = parsed.config;
                filename = parsed.name;
            }
        } catch (e) {
            console.error('Error reading current event from localStorage:', e);
        }

        // If no local event, fetch default from server
        if (!cfg) {
            const cfgResp = await fetch('?load');
            if (cfgResp.status === 401) { location.reload(); return; }
            cfg = await cfgResp.json();
            filename = cfg._filename || '';
        }

        // Always fetch supporting data from server
        const [filesResp, bgLibResp, deltasResp] = await Promise.all([
            fetch('?locationfiles'), fetch('?bglib'), fetch('?beacondeltas')
        ]);
        if (filesResp.ok) locationFiles = await filesResp.json();
        if (bgLibResp.ok) mergeBgLibrary(await bgLibResp.json());
        const beaconDeltas = deltasResp.ok ? await deltasResp.json() : {};

        populateForm(cfg);
        applyBeaconDeltas(cfg._beaconDeltas || beaconDeltas);
        setCurrentEvent(filename, cfg.event || '');
        originalConfig   = cfg;
        isDirty          = false;
        hasServerChanges = false;
        hasMapChanges    = false;
        setStatus('');
    } catch (err) {
        setStatus('Failed to load config', 'error');
        console.error(err);
    }
}

// ── Update live config.yaml (was "Save") ─────────────────────────────────────

function validateConfig(cfg) {
    const errors = [];
    const { lat, lon, zoom } = cfg.map;
    if (isNaN(lat)  || lat  < -90  || lat  > 90)  errors.push('Map: latitude must be −90 to 90');
    if (isNaN(lon)  || lon  < -180 || lon  > 180) errors.push('Map: longitude must be −180 to 180');
    if (isNaN(zoom) || zoom < 0    || zoom > 19)  errors.push('Map: zoom must be 0 to 19');
    (cfg.aidstations || []).forEach((g, i) => {
        if (isNaN(g.lat) || g.lat < -90  || g.lat > 90)   errors.push(`Aid Station ${i+1}: latitude must be −90 to 90`);
        if (isNaN(g.lon) || g.lon < -180 || g.lon > 180)  errors.push(`Aid Station ${i+1}: longitude must be −180 to 180`);
    });
    (cfg.igates || []).forEach((g, i) => {
        if (isNaN(g.lat) || g.lat < -90  || g.lat > 90)   errors.push(`iGate ${i+1}: latitude must be −90 to 90`);
        if (isNaN(g.lon) || g.lon < -180 || g.lon > 180)  errors.push(`iGate ${i+1}: longitude must be −180 to 180`);
    });
    return errors;
}

