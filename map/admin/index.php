            tdAdd.className = 'lf-td lf-td-add';
            if (inList) {
                const ind = document.createElement('span');
                ind.className = 'lf-in-event'; ind.textContent = '✓'; ind.title = 'Already in courses list';
                tdAdd.appendChild(ind);
