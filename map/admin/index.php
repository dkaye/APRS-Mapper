                            delBtn.disabled = false;
                        }
                    } catch { setMsg(msg, 'Network error', false); delBtn.disabled = false; }
                });
            }

            tr.appendChild(tdAct);
            tbody.appendChild(tr);
        });
        table.appendChild(tbody);
        tableWrap.appendChild(table);
    }
