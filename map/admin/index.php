            if (sortCol === 'mtime') return sortAsc ? a.mtime - b.mtime : b.mtime - a.mtime;
            const cmp = String(a[sortCol]).localeCompare(String(b[sortCol]), undefined, { sensitivity: 'base' });
            return sortAsc ? cmp : -cmp;
